    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        
        DefaultTableModel myTable = (DefaultTableModel) jTable1.getModel();
        int capacity = Integer.parseInt(txtMax.getText());
        int Rows = jTable1.getRowCount();
        Items items[] = new Items[Rows];
        
        float value;
        for(int i=0; i<Rows; i++){
            value = Float.parseFloat((String)myTable.getValueAt(i, 1))/Float.parseFloat((String)myTable.getValueAt(i, 0));

            Items novo = new Items(Integer.parseInt((String) myTable.getValueAt(i, 0)), Integer.parseInt((String)myTable.getValueAt(i, 1)), value);
            items[i] = novo;
        }
       
        Items aux;
        for(int i=Rows-1;i>=1;i--){
            for(int j=0;j<i;j++){
                if(items[j].getResult() < items[j+1].getResult()){
                    aux = items[j];
                    items[j] = items[j+1];
                    items[j+1] = aux;
                }
                else if(items[j].getResult() == items[j+1].getResult()){
                    if(items[j].getValue() < items[j+1].getValue()){
                        aux = items[j];
                        items[j] = items[j+1];
                        items[j+1] = aux;
                    }
                }
            }
        }
           
        float [] result = new float[Rows];
        for(int i=0;i<Rows;i++) result[i] = 0;
        
        int c = capacity;
        float temp;
        
        for(int i=0;i<Rows;i++){
            if(items[i].getWeight() <= c){
                result[i] = 1;
                c = c - items[i].getWeight();
            }
            else{
                temp = (float) c/items[i].getWeight();
                result[i] = temp;
                break;
            }
        }
      
        int i=0;
        DefaultTableModel myTable2 = (DefaultTableModel) jTable2.getModel();
        String[] table = new String[3];
        while(result[i] != 0){
            table[0] = String.valueOf(items[i].getWeight());
            table[1] = String.valueOf(items[i].getValue());
            table[2] = String.valueOf(result[i]);
            i++;
            myTable2.addRow(table);
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

