    private void btnCodifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCodifyActionPerformed
        txtCodified.setText(codify(txtOriginal.getText()));
        int original = txtOriginal.getText().length() * 8;
        txtLenght.setText(original + " bits");
        int codified = txtCodified.getText().length();
        txtLenghtCodified.setText(codified+" bits");
        float compressionRate = 100 - ((codified * 100) / original);
        txtCompressionRate.setText(compressionRate+"%");
    }//GEN-LAST:event_btnCodifyActionPerformed

